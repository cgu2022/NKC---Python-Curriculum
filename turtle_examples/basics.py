import turtle
t = turtle.Turtle()
t.speed(1)

#######################################################################################
# movement 100 pixels forward
t.forward(100)

#######################################################################################
# movement 100 pixels backward
t.forward(-100)

#######################################################################################
# right turn 90 degrees
t.right(90)

#######################################################################################
# left turn 90 degrees
t.left(90)

#######################################################################################
# turtle draw speed
t.speed(1) # slowest speed
t.forward(100)
t.speed(10) # second fastest
t.forward(100)
t.speed(0) # fastest
t.forward(100)

#######################################################################################
# pen up test
t.penup()
t.forward(100)

#######################################################################################
# pen down test
t.penup()
t.forward(100)
t.pendown()
t.forward(100)

#######################################################################################
# goto coordinate, turtle starts at (0, 0)
t.goto(10, 100) #goes to point (10, 100)

#######################################################################################
# draw circle
t.circle(5, 360)
t.circle(10, 180) # draws half circle

#######################################################################################
# draw square

for i in range(4):
    t.forward(100)
    t.left(90)

#######################################################################################
# fun optical illusion
x = 5
t.circle(5, 360)
for i in range(100):
	t.circle(x + 3, 180)
	x += 3






